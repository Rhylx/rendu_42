#include <unistd.h>

void	ft_putchar(char c);

void	ft_print_number(char a, char b, char c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
	ft_putchar(',');
	ft_putchar(' ');
}

void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a = '0';
	while (a < '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				ft_print_number(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
	ft_putchar('7');
	ft_putchar('8');
	ft_putchar('9');
}
